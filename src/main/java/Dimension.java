
public class Dimension {

    private int hight;
    private int width;
    private int trankCapacity;

    public Dimension(int hight, int width, int trankCapacity) {

        this.hight = hight;
        this.width = width;
        this.trankCapacity = trankCapacity;
    }

    public int getTrankCapacity() {
        return trankCapacity;
    }


}
